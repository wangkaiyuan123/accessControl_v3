package com.dhht.action.card;

import java.util.List;

import com.dhht.entity.doorguard.DoorCard;

public class CardData {
	private int code = 0;
	private String msg = "";
	// 总条数
	private Long count;
	private List<DoorCard> data;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}
	public List<DoorCard> getData() {
		return data;
	}
	public void setData(List<DoorCard> data) {
		this.data = data;
	}
	
}
